#The max() function returns the item with the highest value max(n1, n2, n3, ...), or the item with the highest value in an iterable.
max_number=max(1,50,-100)
print(max_number)

#If the values are strings, an alphabetically comparison is done.Return the name with the highest value, ordered alphabetically:
max_string=max("manal","asmae","wiam")
print(max_string)

#if the values is an iterable
max_list=max([-50,0,70])
print(max_list)